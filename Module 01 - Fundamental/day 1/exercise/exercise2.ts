const radiusCircle:number = 5
const phi:number = 3.14

const diameterCircle:number = 2 * radiusCircle; // Rumus untuk menghitung Diameter Lingkaran adalah 2 x radius
const circumferenceCircle:number = phi * diameterCircle; // Rumus untuk menghitung Keliling Lingkaran adalah Phi x Diameter
const areaCircle:number = phi * radiusCircle**2; // Rumus untuk menghitung Luas Lingkaran adalah Phi x radius^2

console.log(" ");
console.log("Diameter lingkaran adalah : ", diameterCircle);
console.log("Keliling lingkaran adalah : ", circumferenceCircle);
console.log("Luas lingkaran adalah : ", areaCircle);
console.log(" ");
